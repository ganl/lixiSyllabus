package com.austgl.syllabus.bean;

public class Teacher {
	private String tNo;

	private String tNam;

	private String tGen;

	private String tEma;

	private String tPho;

	private String tDep;

	public String gettNo() {
		return tNo;
	}

	public void settNo(String tNo) {
		this.tNo = tNo;
	}

	public String gettNam() {
		return tNam;
	}

	public void settNam(String tNam) {
		this.tNam = tNam;
	}

	public String gettGen() {
		return tGen;
	}

	public void settGen(String tGen) {
		this.tGen = tGen;
	}

	public String gettEma() {
		return tEma;
	}

	public void settEma(String tEma) {
		this.tEma = tEma;
	}

	public String gettPho() {
		return tPho;
	}

	public void settPho(String tPho) {
		this.tPho = tPho;
	}

	public String gettDep() {
		return tDep;
	}

	public void settDep(String tDep) {
		this.tDep = tDep;
	}

}
